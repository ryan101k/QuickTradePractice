  function poolFor(rate) {
    if (rate >= 0.12) return SURGE.concat(NOISE.slice(0, 2));
    if (rate >= 0.02) return UP.concat(SURGE.slice(6), NOISE.slice(0, 2));
    if (rate > -0.02) return FLAT.concat(NOISE);
    if (rate > -0.1) return DOWN.concat(NOISE.slice(0, 3));
    return CRASH.concat(NOISE.slice(3));
  }

  /* 종목토론방 글 생성. 같은 종목·같은 날엔 같은 글이 나오도록 day 를 시드에 넣는다
   * (새로고침할 때마다 여론이 통째로 바뀌면 이상하니까) */
  function posts(stock, rate, day, count) {
    const n = count || 6;
    const pool = poolFor(rate);
    const base = seed(stock.name + '|' + day);
    const out = [];
    const usedText = new Set(), usedNick = new Set();
    for (let i = 0; i < n * 12 && out.length < n; i++) {
      const h = seed(stock.name + '|' + day + '|' + i);
      const text = pool[(h + base) % pool.length];
      const nick = NICKS[(h >>> 3) % NICKS.length];
      if (usedText.has(text) || usedNick.has(nick)) continue;
      usedText.add(text); usedNick.add(nick);
      out.push({
        nick, text,
        min: (h >>> 5) % 180,                       // 몇 분 전 글인지
        like: (h >>> 7) % (rate >= 0.12 || rate <= -0.1 ? 240 : 40),
        mood: rate >= 0.02 ? 'up' : rate <= -0.02 ? 'down' : 'flat',
      });
    }
    return out.sort((a, b) => a.min - b.min);
  }

